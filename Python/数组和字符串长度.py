# 函数：len()
# 1：作用：返回字符串、列表、字典、元组等长度
# 2：语法：len(str)
# 3：参数：
# str：要计算的字符串、列表、字典、元组等
# 4：返回值：字符串、列表、字典、元组等元素的长度
# 5：实例

# 5.1、计算字符串的长度
s = "hello good boy doiido"
len(s)
# 21

# 5.2、计算列表的元素个数：
l = ['h','e','l','l','o']
len(l)
# 5

# 5.3、计算字典的总长度(即键值对总数)：
d = {'num':123,'name':"doiido"}
len(d)
# 2

# 5.4、计算元组元素个数：
t = ('G','o','o','d')
len(t)
# 4