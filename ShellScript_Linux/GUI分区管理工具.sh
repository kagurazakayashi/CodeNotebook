# GNOME 的硬盘管理工具叫做 ​GNOME Disks。它的正式名称是 ​Disks Utility，在 GNOME 桌面环境中，你可以通过搜索 "Disks" 来找到并启动它。
apt-get install gnome-disk-utility -y
gnome-disks

# GParted 是 Linux 上最流行的图形化分区管理工具，支持多种文件系统（如 ext4、NTFS、FAT32 等），功能强大且易于使用。
apt-get install gparted -y
gparted

# KDE Partition Manager 是 KDE 桌面环境下的分区管理工具，功能与 GParted 类似，但界面更符合 KDE 风格。
apt-get install partitionmanager -y
partitionmanager
