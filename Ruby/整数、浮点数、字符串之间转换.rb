# 浮点数转换成整数，会强行去掉小数点后面的数字
123.45.to_i
# => 123

# 整数转换成浮点数，会添加小数点和0
123.to_f
# => 123.0
 
# 整数转换成字符串
123.to_s
# => "123"
 
# 浮点数转换成字符串
123.45.to_s
# => "123.45"
 
# 浮点数转换成字符串，会去掉最后多余的0
123.1230.to_s
# => "123.123"
 
# 字符串转换成整数，以字符开头的，转换不了返回0
"sharejs.com-001".to_i
# => 0
 
# 以数字开头的字符串转换成浮点数
"123.45sharejs.com".to_f
# => 123.45
 
# 以数字开头的字符串转换成整数
"123.45sharejs.com".to_i
# => 123

